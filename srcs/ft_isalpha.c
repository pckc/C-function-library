#include "libft.h"

/*
**	Description
**	-----------
**	Tests a character to see if it is alphanumeric.
**
**	Parameters
**	----------
**	int c: the character to be tested.
*/

int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
	{
		return (1);
	}
	return (0);
}
